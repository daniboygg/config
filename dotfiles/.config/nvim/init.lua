	desc = "Highlight when yanking (copying) text",
	group = vim.api.nvim_create_augroup("kickstart-highlight-yank", { clear = true }),
	callback = function()
		vim.highlight.on_yank()
	end,
})

-- C compilation settings
vim.api.nvim_create_autocmd("FileType", {
  pattern = "c",
  callback = function()
    -- Set compiler to gcc (enables proper error format)
    vim.cmd("compiler gcc")

    -- Set makeprg to compile the current file
    vim.opt_local.makeprg = "gcc -std=c99 -Werror -o %:t:r ./%:t && ./%:t:r"

    -- Optional: Add a keybinding to compile
    vim.keymap.set("n", "<leader>m", ":make<CR>", { buffer = true, desc = "[M]ake/compile" })
  end,
})

